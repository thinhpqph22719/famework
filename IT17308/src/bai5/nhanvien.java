
package bai5;

public class nhanvien {
    String ten;
    String luong;

    public nhanvien() {
    }

    public nhanvien(String ten, String luong) {
        this.ten = ten;
        this.luong = luong;
    }

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

    public String getLuong() {
        return luong;
    }

    public void setLuong(String luong) {
        this.luong = luong;
    }
    
}
