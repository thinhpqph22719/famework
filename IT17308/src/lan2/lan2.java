    ArrayList<myjob> listmj = new ArrayList<myjob>();

    void fillTable() {
        DefaultTableModel mol = (DefaultTableModel) tbl_table.getModel();
        mol.setRowCount(0);
        for (myjob x : listmj) {
            Object[] data = new Object[]{
                x.getTenCV(), x.getTenDN(), x.getHocTienSy(), x.getThunhap(),x.getKetLuan()
            };
            mol.addRow(data);
        }
    }

    myjob readForm() {
        String tencv, tendn, hoctiensy;
        double thunhap;
        tencv = txt_ten.getText();
        tendn = (String) cbo_tendn.getSelectedItem();
        if (rdo_co.isSelected()) {
            hoctiensy = "có";
        } else {
            hoctiensy = "không";
        }
        thunhap = Double.parseDouble( txt_thunhap.getText());
        return new myjob(tencv, tendn, hoctiensy, thunhap);
    }
