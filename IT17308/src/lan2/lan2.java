    @Override
    public void run() {
        while (true) {
            te = Calendar.getInstance().getTimeInMillis() / 1000;
            String time = (te - ts) + "";
            lbl_time.setText("thoi gian chay:" + time);
            String d,M,y,a1;
            int a;
            Calendar cl = Calendar.getInstance();
            d= cl.get(Calendar.HOUR_OF_DAY)+"";
            M= cl.get(Calendar.MINUTE)+"";
            y= cl.get(Calendar.SECOND)+"";
            a = cl.get(Calendar.AM_PM);
            if(a==1){
                a1="PM";
            }else{
            a1 ="AM";
            }
            lbl_time2.setText("thoi gian:"+d+ ":"+M+":"+y+ a1);
            try {
                Thread.sleep(1000);
            } catch (InterruptedException ex) {
                Logger.getLogger(lan2.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }
